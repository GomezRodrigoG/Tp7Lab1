    private void jbInscribirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbInscribirActionPerformed
        // TODO add your handling code here:
        
        String inputMate = jcbMaterias.getSelectedItem().toString();
        String inputAlumno = jcbAlumno.getSelectedItem().toString();
        
        Materia materia = null;
        Alumno alumno = null;
        
        for(Materia auxMate: materias){
            if(auxMate.getNombre().equals(inputMate)){
                materia = auxMate;
                break;
            }
        }
        
        for(Alumno auxAlumno: alumnos){
            String completeName = auxAlumno.getApellido() + " " + auxAlumno.getNombre();
            if(completeName.equals(inputAlumno)){
                alumno = auxAlumno;
                break;
            }
        }
        
        if(alumno.agregarMateria(materia)){
            JOptionPane.showMessageDialog(this, "Inscripcion realizada con Ã©xito.");
        } else {
            JOptionPane.showMessageDialog(this, "No se puede crear inscripcion. El alumno ya estaba inscripto en esta materia");
        }
    }//GEN-LAST:event_jbInscribirActionPerformed


